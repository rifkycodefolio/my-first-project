jumlah_buku = 10
jumlah_buku_dibaca = 0
print(f'Jumlah buku yang sudah dibaca {jumlah_buku_dibaca}')
while jumlah_buku_dibaca < jumlah_buku:
    jumlah_buku_dibaca = jumlah_buku_dibaca + 1
    print(f'Buku ke {jumlah_buku_dibaca} sudah dibaca')
print(f'Jumlah buku yang sudah dibaca {jumlah_buku_dibaca}')
